# 1. Definiamo gli strumenti (Tools) dell'IA
search = DuckDuckGoSearchRun()
python_repl = PythonREPL()

# Strumento per permettere all'IA di fare calcoli complessi in Python
python_tool = Tool(
    name="Python_Evaluator",
    description="Usa questo strumento per eseguire calcoli statistici, simulazioni Monte Carlo o creare grafici.",
    func=python_repl.run
)

# Strumento per cercare dati macroeconomici aggiornati
search_tool = Tool(
    name="Market_Search",
    description="Usa questo strumento per trovare dati economici attuali come tassi di interesse, PIL o inflazione.",
    func=search.run
)

# 2. Inizializziamo il "Cervello" (LLM)
llm = ChatOpenAI(model="gpt-4o", temperature=0)

# 3. Creiamo l'Agente
agent = initialize_agent(
    tools=[search_tool, python_tool],
    llm=llm,
    agent=AgentType.ZERO_SHOT_REACT_DESCRIPTION,
    verbose=True # Ti permette di vedere il "ragionamento" dell'IA in tempo reale
)

# 4. Esempio di utilizzo autonomo
domanda = """
Trova l'attuale tasso di interesse della BCE e le previsioni di crescita del PIL italiano per il 2026. 
Poi, calcola la probabilità di una recessione tecnica (PIL negativo per due trimestri) 
facendo una simulazione con una volatilità del 1.5%.
"""

print("L'Agente sta analizzando il mercato...")
risposta = agent.run(domanda)

print("\n--- ANALISI FINALE ---")
