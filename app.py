import numpy as np
import matplotlib.pyplot as plt

def simulazione_macroeconomica(pil_attuale, crescita_attesa, volatilita, tassi_incremento, simulazioni=10000):
    """
    Simula la probabilità dell'andamento del PIL basato su un aumento dei tassi.
    """
    # L'impatto stimato: ogni 1% di aumento tassi toglie lo 0.5% di crescita (esempio)
    impatto_tassi = tassi_incremento * -0.5
    media_finale = crescita_attesa + impatto_tassi
    
    # Generazione di 10.000 scenari basati su una distribuzione normale
    scenari = np.random.normal(media_finale, volatilita, simulazioni)
    
    # Calcolo della probabilità di recessione (PIL < 0)
    recessione = scenari[scenari < 0]
    probabilita_recessione = (len(recessione) / simulazioni) * 100
    
    return scenari, probabilita_recessione

# --- PARAMETRI DI INPUT ---
pil_italia_2026 = 100          # Punto di partenza base
crescita_stimata = 1.2         # +1.2% previsto
volatilita_mercato = 0.8       # Quanto variano i dati storicamente
aumento_tassi_bce = 0.75       # Ipotizziamo +0.75% di tassi

# Esecuzione
risultati, prob_rec = simulazione_macroeconomica(pil_italia_2026, crescita_stimata, volatilita_mercato, aumento_tassi_bce)

# --- VISUALIZZAZIONE ---
plt.figure(figsize=(10, 6))
plt.hist(risultati, bins=50, color='skyblue', edgecolor='black', alpha=0.7)
